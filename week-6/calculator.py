def add(n1, n2):
    return n1 + n2


def substract(n1, n2):
    return n1 - n2


def multiply(n1, n2):
    return n1 * n2


def divide(n1, n2):
    return n1 / n2


if __name__ == "__main__":
    r1 = add(1, 3)
    r2 = substract(1, 3)
    r3 = multiply(1, 3)
    r4 = divide(1, 3)
    print(r1, r2, r3, r4)